// Patern program for to print all star (square)
class patternAllstar
{
	public static void main(String[] args) 
	{
	   int num=4;

	   for(int i=1;i<=num;i++)
	     {
	     	for(int j=1;j<=num;j++)

	     	{
	     		System.out.print("* ");
	     	}
	     	System.out.println();
	     }	

	}
}


